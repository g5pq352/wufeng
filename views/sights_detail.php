		霧峰林家是台灣五大家族之一，因其發跡於霧峰（古稱阿罩霧，今台灣臺中市霧峰區）而得名。自19世紀中期以來，林家因協助平定太平天國、戴潮春事件並參與中法戰爭，而掌有數千精良兵勇，以及樟腦專賣權等特權，掌控了中台灣大量的田地，進而成為清領時期以降台灣社會最具影響力的家族之一。日本時期，霧峰林家與基隆顏家、板橋林家、鹿港辜家、高雄陳家並列為「台灣五大家族」。<br>
		<br>
		<div class="-mx-4"><img src="images/message-1.jpg"></div>
		<br>
		國定古蹟-霧峰林家宅園，係指霧峰林家位於阿罩霧地區的園林與宅邸建築之總稱，霧峰林家建築古蹟範圍：包括下厝系統、頂厝系統以及萊園三大部份。而霧峰林家宮保第園區開放區域是下厝所屬之宮保第、大花廳及草厝等三個建築群落。<br>
		<br>
		<div class="-mx-4"><img src="images/message-2.jpg"></div>
		<br>
		『宮保第』-霧峰林家宅園下厝建築群之一，係由林文察(1828-1864)於咸豐八年(1858)首建現存第三進及左右內外護龍，直至其帶領台勇前往漳州攻打太平天國戰歿於萬松關，於同治三年(1864)獲詔追封[太子少保]，始稱宮保第；建築整體空間左右對稱，為五進十一開間格局。第一進與第二進門廳為林朝棟於同治九年(1870)至光緒九年(1883)增建而成，而第四進與第五進也在之後陸續增建，於光緒二十一年(1895)甲午割日前完成。『宮保第』為臺灣地區僅存清代一品官宅。<br>
		<br>
		『大花廳』-霧峰林家宅園下厝建築群之一，五開間三落格局，福州式戲台與特色卷棚作法，為全台唯一，因其八角藻井中雕琢有花中之王-牡丹，具豐腴之姿、富貴姿態，於頂心明鏡位置盛開，有花開富貴之意，因而取名「大花廳」。光緒十六年(1890)起建，光緒二十年(1894)竣工，林朝棟任撫墾局局長時，因軍事會議及公共宴席使用需求而興建之，更是林家宴客與看戲空間，當時還有專屬戲班，如同現今「招待所」、「私人歌劇院」，係霧峰林家鼎盛時期象徵。<br>
		<br>
		『草厝』-霧峰林家宅園下厝建築群之一，霧峰林家起家厝，草厝建築為正身和護龍組成的三合院草屋，並有穀倉和門樓，外面則有大片庭園空間，約在道光17年（1837）起興建，是霧峰林家早期重要的生活空間。象徵林家篳路藍縷白手起家階段，因部分早在日本時期就毀壞，經多次調查研究比對老照片，並以電腦模擬、復原相關位置，才確定修復的方式，將以1930年照片為修復基準，並以原工法、材料進行復原，其中原本作為「公媽廳」的正身，修復時更將鋪上茅草屋頂，是目前古蹟修復少見的方式。已於2020年修復完工開放。 
